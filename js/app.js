function generate_person(){
    let rn = Math.floor(Math.random()*100);
    let rs1 = Math.floor(Math.random()*100);
    let rs2 = Math.floor(Math.random()*100);
    let ri = Math.floor(Math.random()*50);
    let rr = Math.floor(Math.random()*2);
    let checkedSex;
    
    if (document.getElementById("optionsRadios1").checked){
        checkedSex = "male";
    } else if (document.getElementById("optionsRadios2").checked){
        checkedSex = "female";
    } else if (document.getElementById("optionsRadios3").checked){
        checkedSex = "both";
    }
    
    document.getElementById("generate-name").innerHTML = "";
    
    if (document.getElementById("flexCheckChecked1").checked){
        if ((checkedSex == "male")||((checkedSex == "both")&&(rr===0))){
            document.getElementById("generate-name").innerHTML = maleNames[rn]
        } else if ((checkedSex == "female")||((checkedSex == "both")&&(rr===1))){
            document.getElementById("generate-name").innerHTML = femaleNames[rn]
        }
    }
    
    if (document.getElementById("flexCheckChecked2").checked){
        document.getElementById("generate-name").innerHTML += " " + surNames[rs1] + " " + surNames[rs2];
    }
    
    if (document.getElementById("flexCheckChecked3").checked){
        if ((checkedSex == "male")||((checkedSex == "both")&&(rr===0))){
            document.getElementById("generate-img").src = maleImages[ri];
        } else if ((checkedSex == "female")||((checkedSex == "both")&&(rr===1))){
            document.getElementById("generate-img").src = femaleImages[ri];
        }
    } else {
        document.getElementById("generate-img").src = "";
    }
}